function soma(numero1, numero2) {
    let soma = numero1 + numero2
    return 'A soma dos números é: ' + soma;
}
const resultadoDaSoma = soma(1, 3)
console.log(resultadoDaSoma)

function subtracao(numero3, numero4) {
    let subtracao = numero3 - numero4
    return 'A subtração dos números é: ' + subtracao;
}
const resultadoDaSubtracao = subtracao(10, 5)
console.log(resultadoDaSubtracao)

function divisao(numero5, numero6) {
    let divisao = numero5 / numero6
    return 'A divisão dos números resulta em: ' + divisao;
}
const resultadoDaDivisao = divisao(20, 10)
console.log(resultadoDaDivisao)

